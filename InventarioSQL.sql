CREATE DATABASE INVENTARIO

GO

USE INVENTARIO

GO

CREATE TABLE PRODUCTOS(
    Clave VARCHAR(6) PRIMARY KEY NOT NULL,
    Nombre VARCHAR(MAX) NOT NULL,
    Precio DECIMAL(18,2) NOT NULL
)

GO

CREATE TABLE SUCURSALES(
    Clave VARCHAR(3) PRIMARY KEY NOT NULL,
    Nombre VARCHAR(MAX) NOT NULL
)

GO

CREATE TABLE EXISTENCIAS(
    ID INT IDENTITY(1,1) PRIMARY KEY,
    Sucursal VARCHAR(3) NOT NULL,
    Producto VARCHAR(6) NOT NULL,
    Cantidad INT NOT NULL
)

GO

ALTER TABLE EXISTENCIAS
    ADD CONSTRAINT FK_SUCURSAL FOREIGN KEY (Sucursal)
    REFERENCES SUCURSALES (Clave)

GO

ALTER TABLE EXISTENCIAS
    ADD CONSTRAINT FK_PRODUCTO FOREIGN KEY (Producto)
    REFERENCES PRODUCTOS (Clave)

